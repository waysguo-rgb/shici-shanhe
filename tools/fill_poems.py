EXCERPT_ONLY = [
    '琵琶行',
    '正气歌',
    '秋兴八首',
    '岳阳楼记',
    '醉翁亭记',
    '滕王阁序',
]

def main():
    with open(JSON_PATH, encoding='utf-8') as f:
        data = json.load(f)

    filled = 0
    excerpted = 0
    for loc in data:
        for p in loc['poems']:
            t = p['title']
            if t in FULL_FILLS:
                before = len(p['lines'])
                p['lines'] = FULL_FILLS[t]
                after = len(p['lines'])
                print(f"FULL  {loc['name']}/{t}: {before} -> {after} 行")
                filled += 1
            elif t in EXCERPT_ONLY:
                if not p.get('excerpt', False):
                    p['excerpt'] = True
                    excerpted += 1
                    print(f"EXCERPT  {loc['name']}/{t} ({len(p['lines'])} 行, 标节选)")

    with open(JSON_PATH, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"\n完成: 补全 {filled}, 标节选 {excerpted}")

if __name__ == '__main__':
    main()
